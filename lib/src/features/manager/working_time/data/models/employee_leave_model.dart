import 'dart:convert';
import '../../domain/entities/entities.dart';

List<EmployeesLeaveModel> employeesLeaveModelFromJson(String str) =>
    List<EmployeesLeaveModel>.from(
        json.decode(str).map((x) => EmployeesLeaveModel.fromJson(x)));

String employeesLeaveModelToJson(List<EmployeesLeaveModel> data) =>
    json.encode(List<dynamic>.from(data.map((x) => x.toJson())));

class EmployeesLeaveModel extends EmployeesLeaveEntity {
